package com.velocity.java.starpatternprograms;

public class StarpatternInFourteenNumber {

	public static void main(String[] args) {
		int i;
		for (i = 0; i < 5; i++) {
			System.out.println("*    *");
		}
		System.out.println("*    * * * * * *");
		for (i = 0; i < 5; i++) {
			System.out.println("*              *");
		}

	}

}
