package com.velocity.java.starpatternprograms;

public class StarpatternInEighteenNumber {

	public static void main(String[] args) {
		System.out.println("*   * * * * *");
		for (int i = 0; i <= 2; i++) {
			System.out.println("*   *       *");
		}
		System.out.print("*   * * * * * \n");

		for (int i = 0; i <= 2; i++) {
			System.out.println("*   *       *");
		}
		System.out.print("*   * * * * * ");

	}

}
