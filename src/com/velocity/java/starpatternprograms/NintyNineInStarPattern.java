package com.velocity.java.starpatternprograms;

public class NintyNineInStarPattern {

	public static void main(String[] args) {
		int i;
		System.out.println("* * * * *   * * * * *");
		for (i = 0; i < 4; i++) {
			System.out.println("*       *   *       *");
		}
		System.out.println("* * * * *   * * * * *");
		for (i = 0; i < 4; i++) {
			System.out.println("        *           *");
		}
		System.out.println("* * * * *   * * * * *");

	}

}
